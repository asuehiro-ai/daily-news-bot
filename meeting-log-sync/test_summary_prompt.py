#!/usr/bin/env python3
"""build_log_summary_prompt() が実際にどんな要約を出すかを、ダミーの議事録テキストで確認する
一回限りの動作確認スクリプト。実際の面談データは一切使わない。

必須環境変数: ANTHROPIC_API_KEY

使い方: python meeting-log-sync/test_summary_prompt.py
"""

import os
import sys

from plaud_client import build_log_summary_prompt, claude_summarize

# PLAUDの自動要約にありがちな「話者が不明瞭・双方の発言が混在・長い」を模したダミーテキスト。
# 登場する会社名・数値・人物はすべて架空。
SAMPLE_RAW_SUMMARY = """
本日はお時間いただきありがとうございます。弊社は事業承継・M&A支援を専門にしておりまして、
今回は貴社の状況を伺いながら、どのような選択肢があるかご説明できればと思っております。
まず御社の概要なんですが、創業から40年ほど経っていて、社長は70歳を超えられていて、
後継者が社内にはいらっしゃらないということでよろしかったでしょうか。
はい、その通りです。息子は別の仕事をしていて、会社を継ぐ気はないと言っています。
従業員は今28名で、主要取引先は3社で売上の6割を占めています。直近の決算は
売上高が4.2億円、営業利益が3千万円程度です。ただ、去年は大口の取引先が1社離れてしまって、
売上が2割ほど落ちました。
なるほど、承知しました。M&Aによる第三者承継という選択肢についてご説明しますと、
一般的には企業概要書を作成して、匿名の形で買い手候補にアプローチし、興味を持っていただいた
企業とトップ面談を行い、条件面を調整しながら進めていく流れになります。期間としては
半年から1年程度かかることが多いです。
費用はどれくらいかかるんですか。
着手金や中間金がかかるケースと、成功報酬型のみのケースがありますが、弊社の場合は
基本的に成功報酬型でご案内しております。詳細は後日改めて資料をお送りします。
従業員の雇用は継続されるんでしょうか。それが一番気になっているところです。
そこは非常に重要なポイントですので、トップ面談の段階で必ず確認し、条件として
盛り込むようにしています。
分かりました。前向きに検討したいので、まずは企業概要書の作成をお願いできますか。
承知しました。それでは来週、決算書等の資料をお送りいただけますでしょうか。
分かりました。あと、この話は社内では専務にしか伝えていないので、他言無用でお願いします。
もちろんです、秘密保持契約も締結させていただきますのでご安心ください。
"""


def main():
    api_key = os.environ.get("ANTHROPIC_API_KEY")
    if not api_key:
        print("ANTHROPIC_API_KEY が未設定です")
        sys.exit(1)

    prompt = build_log_summary_prompt(SAMPLE_RAW_SUMMARY.strip())
    result = claude_summarize(prompt, api_key)

    print("=== 入力（ダミーの生テキスト。PLAUD自動要約を模したもの） ===")
    print(SAMPLE_RAW_SUMMARY.strip())
    print()
    print("=== 出力（新しい要約プロンプトの結果） ===")
    print(result if result else "（要約生成に失敗しました）")


if __name__ == "__main__":
    main()
